#include <unistd.h>
// #include <stdio.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	d;
	long	n;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	i = 1;
	while (n / i >= 10)
		i *= 10;
	while (i)
	{
		d = '0' + (n / i);
		write(1, &d, 1);
		n %= i;
		i /= 10;
	}
}

